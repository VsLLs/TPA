    private void btn_imovelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imovelActionPerformed
        // TODO add your handling code here:
        if (controller.temLetra(c_imovel_cod.getText())) {
            JOptionPane.showMessageDialog(null, "Código não pode conter letras!");
        } else if (c_imovel_cod.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Código curto demais!");
        } else if (controller.temNumero(c_imovel_vend.getText())) {
            JOptionPane.showMessageDialog(null, "Vendedor não pode conter números!");
        } else if (c_imovel_vend.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Vendedor curto demais!");
        } else if (c_imovel_end.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Endereço curto demais!");
        } else if (controller.temLetra(c_imovel_tipo.getText())) {
            JOptionPane.showMessageDialog(null, "Tipo só pode conter números!");
        } else if (c_imovel_tipo.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Tipo curto demais!");
        } else if (controller.temLetra(c_imovel_quartos.getText())) {
            JOptionPane.showMessageDialog(null, "Quartos só pode conter números!");
        } else if (c_imovel_quartos.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Quartos curto demais!");
        } else if (controller.temLetra(c_imovel_valor.getText())) {
            JOptionPane.showMessageDialog(null, "Valor só pode conter números!");
        } else if (c_imovel_valor.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Valor curto demais!");
        } else if (controller.findCliente(c_imovel_vend.getText()) == null) {
            JOptionPane.showMessageDialog(null, "Vendedor inválido!");
        } else {
            int cod = Integer.parseInt(c_imovel_cod.getText());
            int tipo = Integer.parseInt(c_imovel_tipo.getText());
            int quartos = Integer.parseInt(c_imovel_quartos.getText());
            float valor = Float.valueOf(c_imovel_valor.getText());

            Imovel newImovel = new Imovel(cod,
                    controller.findCliente(c_imovel_vend.getText()),
                    c_imovel_end.getText(),
                    tipo,
                    quartos,
                    valor
            );
            if (controller.findImovel(newImovel) != null) {
                JOptionPane.showMessageDialog(null, "Imovel já existe!");
            } else {
                controller.addImovel(newImovel);
                c_imovel_cod.setText("");
                c_imovel_vend.setText("");
                c_imovel_end.setText("");
                c_imovel_tipo.setText("");
                c_imovel_quartos.setText("");
                c_imovel_valor.setText("");
                updateListImovel();
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            }
        }
    }//GEN-LAST:event_btn_imovelActionPerformed


