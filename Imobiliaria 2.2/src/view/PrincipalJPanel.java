    private void btn_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_clienteActionPerformed
// TODO add your handling code here:
        if (controller.temNumero(c_cliente_nome.getText())) {
            JOptionPane.showMessageDialog(null, "Nome não pode conter números!");
        } else if (c_cliente_nome.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Nome curto demais!");
        } else if (controller.temLetra(c_cliente_tel.getText())) {
            JOptionPane.showMessageDialog(null, "Telefone não pode conter letras!");
        } else if (c_cliente_tel.getText().length() < 7) {
            JOptionPane.showMessageDialog(null, "Telefone deve conter no minimo 8 números!");
        } else if (c_cliente_end.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Endereço curto demais!");
        } else if (controller.temArroba(c_cliente_email.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Email deve conter @!");
        } else if (c_cliente_email.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Email curto demais!");
        } else {
            Cliente newCliente = new Cliente(c_cliente_nome.getText(),
                    c_cliente_end.getText(),
                    c_cliente_tel.getText(),
                    c_cliente_email.getText()
            );
            if (controller.findCliente(c_cliente_email.getText()) != null) {
                JOptionPane.showMessageDialog(null, "Cliente já existe!");
            } else {
                controller.addClientes(newCliente);
                c_cliente_nome.setText("");
                c_cliente_end.setText("");
                c_cliente_tel.setText("");
                c_cliente_email.setText("");
                updateListClientes();
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            }

        }
    }//GEN-LAST:event_btn_clienteActionPerformed

